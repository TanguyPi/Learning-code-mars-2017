// Créer une variable de type NUMBER de 10

    // Opérateur d'affectation
    var myNumber = 10;
    console.log(myNumber);

    // Opérateur d'accrémntation : ++  / (On ajoute le numéro d'au-dessus.)
    myNumber++;
    console.log(myNumber);

    // Opérateur de decrémentation : --  (On diminue de 1 la valeur )
    myNumber--;
    console.log(myNumber);

    // Opérateur d'addition/concaténation : + (On additionne 2 chiffres) 
    var myAdd = 7+5;
    console.log(myAdd); 

    var myConc = 'Bonjour ' + 'mon ' + 'pote ';
    console.log(myConc);

    //Opérateur de soustraction : - 
    var mySous = 7-5;
    console.log(mySous);

    // Opérateur de multiplication: *
    var myMult = 7*5;
    console.log(myMult);

    // Opérateur de division : /
    var myDivi = 14/3;
    console.log(myDivi);

    // Opérateur modulo : %    (Le modulo c'est le reste d'une division)
    var myModulo = 16 % 12;
    console.log(myModulo);
    
    // Opérateur d'addition et d'affectation: +=
    var first = 10;
    var second = 5;

    first += second;   
    console.log(first);

    // Opérateur de soustraction et d'affectation: -=
    second -= first;
    console.log(second);